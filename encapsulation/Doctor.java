package com.example.demo;
public class Doctor
{
private int docId;
private String docName;

public Doctor()
{
}

public void setDocID(int id)
{
docId=id;
}

public int getDocID()
{
return docId;
}

public void setDocName(String name)
{
docName=name;
}

public String getDocName()
{
return docName;
}
}